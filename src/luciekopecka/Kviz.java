    private void setCurrentOtazka() {
        this.currentOtazka = this.dotaznik.getOtazkaOnIndex(this.currentOtazkaIndex);
        this.textOtazky.setText(this.currentOtazka.getTextOtazky());
        this.hodnota.setText("Hodnota: " + this.currentOtazka.getHodnota());
        this.currentOdpovedi = this.currentOtazka.getOdpovedi();
        
        for(javax.swing.JButton button : this.odpovediButtons) {
            PomocneFunkce.setButtonVisibilty(button, false);
        }
        
        PomocneFunkce.setButtonVisibilty(continueButton, false);
        
        int i = 0;
        for(; i < this.currentOdpovedi.size(); i++) {
            PomocneFunkce.setButtonVisibilty(this.odpovediButtons.get(i), true);
            this.odpovediButtons.get(i).setText(this.currentOdpovedi.get(i).getTextOdpovedi());
            this.odpovediButtons.get(i).setForeground(Color.BLACK);
        }
    }
    
    private void decideAnswer(int chosenAnswer) {
        if(chosenAnswer >= this.currentOdpovedi.size())
            return;
        int spravneIndex = this.currentOtazka.getCorrectOdpovedIndex();
        if(chosenAnswer == spravneIndex) {
            this.currentSkore += this.currentOtazka.getHodnota();
            this.skore.setText("Sk처re: " + this.currentSkore);
            this.odpovediButtons.get(chosenAnswer).setForeground(Color.GREEN);
        }
        else {
            this.odpovediButtons.get(chosenAnswer).setForeground(Color.RED);
            this.odpovediButtons.get(spravneIndex).setForeground(Color.GREEN);
        }
        PomocneFunkce.setButtonVisibilty(continueButton, true);
        this.continueButton.setText("Pokra훾ovat");
    }
    
